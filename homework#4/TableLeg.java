public class TableLeg {
    String TableLegColor;
    String TableLegShape;
    int TableLegWidth;
    int TableLegLength;

    public TableLeg(String TableLegColor, String TableLegShape, int TableLegWidth, int TableLegLength) {
        this.TableLegColor = TableLegColor;
        this.TableLegShape = TableLegShape;
        this.TableLegWidth = TableLegWidth;
        this.TableLegLength = TableLegLength;
    }
}
